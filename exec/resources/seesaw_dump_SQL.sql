    JOIN mission AS m ON mm.mission_id = m.mission_id
    SET mm.member_mission_status = CASE
        WHEN (
        SELECT COUNT(*)
        FROM record AS r
        WHERE r.member_email = mm.member_email
        AND r.mission_id = mm.mission_id
        AND r.record_status = 1
        ) >= (m.mission_total_cycle * 0.8) THEN 2
