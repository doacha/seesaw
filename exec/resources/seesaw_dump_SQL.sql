  # 어제의 날짜 계산
  DECLARE yesterday DATE;
  SET yesterday = DATE_SUB(CURRENT_DATE, INTERVAL 1 DAY);

  # record 테이블에서 어제 회차가 종료됐고 성공한 경우에 대해 memberMissionIsSaving 업데이트
